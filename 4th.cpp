#include<iostream>
using namespace std;

int main() {

    int a = 4, b = 6;

    cout << "Operator in c++" << endl;
    cout << "type of arithmetic operator in cpp" << endl;
    "\n";
    cout << "the value of a+b is: " << a + b << endl;
    cout << "the value of a-b is: " << a - b << endl;
    cout << "the value of a%b is: " << a % b << endl;
    cout << "the value of a*b is: " << a * b << endl;
    cout << "the value of a/b is: " << a / b << endl;
    cout << "the value of ++a is: " << ++a << endl;
    cout << "the value of a-- is: " << a-- << endl;

    return 0;
}
